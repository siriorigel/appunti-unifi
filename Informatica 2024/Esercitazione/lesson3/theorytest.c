#include <stdio.h>

int main() {
    int a, b, c; // Dichiarazione di tre variabili intere

    a = 6;       // Assegnamento del valore 6 alla variabile a
    b = a + 1;   // Assegnamento del valore di a incrementato di 1 alla variabile b
    c = 7;       // Assegnamento del valore 7 alla variabile c

    printf("%d", b); // Stampa il valore di b

    return 0; // Il programma termina con successo
}
